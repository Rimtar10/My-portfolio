/**
 * Small floating star particles scattered across a section's entire
 * background, in a wide mix of sizes (tiny 1px flecks up to bold 8px
 * dots) so the field reads as varied rather than uniform. Each one
 * drifts on a slow, looping path (not just a still twinkle), echoing the
 * motion of the nodes in the hero's 3D scene. Shown on every screen
 * size. Purely decorative — the parent section must be `relative` (and
 * ideally `overflow-hidden`) for this to anchor correctly.
 *
 * `count` trims the shared star list down for sections that should read
 * lighter (e.g. Hero, Skills, Leadership); omit it for sections that
 * should read densest (e.g. About, Projects), which pulls from the full
 * pool.
 */
export function Starfield({ count }: { count?: number }) {
  const stars = count ? STARS.slice(0, count) : STARS;
  return (
    <div aria-hidden className="pointer-events-none absolute inset-0">
      {stars.map((s, i) => (
        <motion.span
          key={i}
          className="absolute rounded-full"
          style={{
            left: s.left,
            top: s.top,
            width: s.size,
            height: s.size,
            backgroundColor: s.color,
          }}
          animate={{
            opacity: [0.2, 0.95, 0.2],
            scale: [0.85, 1.2, 0.85],
            x: [0, s.dx, 0],
            y: [0, s.dy, 0],
          }}
          transition={{
            duration: s.duration,
            delay: s.delay,
            repeat: Infinity,
            repeatType: "mirror",
            ease: "easeInOut",
          }}
        />
      ))}
    </div>
  );
}
